from launch import LaunchDescription
from launch.actions import DeclareLaunchArgument
from launch.substitutions import LaunchConfiguration
from launch_ros.actions import Node

def generate_launch_description():
    use_sim_time = LaunchConfiguration('use_sim_time', default='true')
    map_yaml = LaunchConfiguration('map', default='/ABS/path/campus_map.yaml')
    params = LaunchConfiguration('params', default='/ABS/path/nav2_params.yaml')

    return LaunchDescription([
        DeclareLaunchArgument('use_sim_time', default_value='true'),
        DeclareLaunchArgument('map', default_value='/home/deyung/ros2_newws/src/mymasterproject/maps/map.yaml'),
        DeclareLaunchArgument('params', default_value='/home/deyung/ros2_newws/src/mymasterproject/src/world/nav2_params.yaml'),

        Node(package='nav2_map_server', executable='map_server', name='map_server',
             output='screen', parameters=[{'use_sim_time': use_sim_time, 'yaml_filename': map_yaml}]),
        Node(package='nav2_amcl', executable='amcl', name='amcl',
             output='screen', parameters=[params]),

        Node(package='nav2_planner', executable='planner_server', name='planner_server',
             output='screen', parameters=[params]),
        Node(package='nav2_controller', executable='controller_server', name='controller_server',
             output='screen', parameters=[params]),
        Node(package='nav2_bt_navigator', executable='bt_navigator', name='bt_navigator',
             output='screen', parameters=[params]),
        Node(package='nav2_waypoint_follower', executable='waypoint_follower', name='waypoint_follower',
             output='screen', parameters=[params]),
        Node(package='nav2_lifecycle_manager', executable='lifecycle_manager', name='lifecycle_manager_navigation',
             output='screen', parameters=[{'use_sim_time': use_sim_time,
                                           'autostart': True,
                                           'node_names': [
                                              'map_server','amcl',
                                              'planner_server','controller_server',
                                              'bt_navigator','waypoint_follower'
                                           ]}]),
    ])
